char	*ft_strrev(char *str)
{
	int newstr[];
	int i = 0;
	while(str[i] != '\n')
	{
		i++;
	}
	int j = 0;
	while(i >= 0)
	{
		newstr[j] = str[i];
		j++;
		i--;
	}
	str = newstr;
	return str;
}
